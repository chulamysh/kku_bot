def get_emoji(article: dict) -> str:
    """
    Повертає підходящий емодзі для статті на основі назви та початку тексту.
    """
    name_lower = article.get("name", "").lower()
    text_preview = article.get("text", "")[:300].lower()
    combined = name_lower + " " + text_preview

    for keywords, emoji in EMOJI_RULES:
        for kw in keywords:
            if kw in combined:
                return emoji

    return "📋"  # дефолтний емодзі
